export function AsciiArtGeneratorTool() {
  const [inputText, setInputText] = useState("Hello");
  const [selectedFont, setSelectedFont] = useState<AsciiFont>(FONTS[0]!);
  const [copied, setCopied] = useState(false);

  const asciiOutput = renderAscii(inputText, selectedFont);

  const handleCopy = useCallback(async () => {
    if (!asciiOutput) return;
    try {
      await navigator.clipboard.writeText(asciiOutput);
      setCopied(true);
      toast.success("ASCII art copied to clipboard!");
      ToolEvents.resultCopied();
      setTimeout(() => setCopied(false), 2000);
    } catch {
      toast.error("Could not copy to clipboard.");
    }
  }, [asciiOutput]);

  const handleClear = useCallback(() => {
    setInputText("");
  }, []);

  return (
    <div className="max-w-4xl mx-auto space-y-6">
      {/* Input row */}
      <motion.div
        initial={{ opacity: 0, y: 20 }}
        animate={{ opacity: 1, y: 0 }}
        transition={{ delay: 0.1 }}
        className="rounded-2xl border border-border/50 bg-muted/30 p-6 space-y-4"
      >
        <div className="flex items-center gap-2 mb-2">
          <Terminal className="h-4 w-4 text-brand" />
          <span className="text-sm font-medium text-muted-foreground">
            Enter your text (max 50 characters)
          </span>
        </div>

        <Input
          value={inputText}
          onChange={(e) => {
            setInputText(e.target.value.slice(0, 50));
            ToolEvents.toolUsed("type");
          }}
          placeholder="Type something..."
          className="text-lg font-mono"
          maxLength={50}
        />

        {/* Font selector */}
        <div className="flex flex-wrap gap-2 pt-2">
          {FONTS.map((font) => (
            <button
              key={font.name}
              onClick={() => {
                setSelectedFont(font);
                ToolEvents.toolUsed("font-change");
              }}
              className={`px-3 py-1.5 rounded-full text-xs font-medium border transition-all ${
                selectedFont.name === font.name
                  ? "bg-brand text-white border-brand shadow-sm shadow-brand/25"
                  : "bg-muted/50 text-muted-foreground border-border/50 hover:border-brand/50 hover:text-foreground"
              }`}
            >
              {font.name}
            </button>
          ))}
        </div>
      </motion.div>

      {/* Output area */}
      <motion.div
        initial={{ opacity: 0, y: 20 }}
        animate={{ opacity: 1, y: 0 }}
        transition={{ delay: 0.2 }}
        className="rounded-2xl border border-border/50 bg-muted/30 overflow-hidden"
      >
        {/* Toolbar */}
        <div className="flex items-center justify-between px-5 py-3 border-b border-border/50 bg-muted/50">
          <div className="flex items-center gap-1.5">
            <div className="w-3 h-3 rounded-full bg-red-400/70" />
            <div className="w-3 h-3 rounded-full bg-yellow-400/70" />
            <div className="w-3 h-3 rounded-full bg-green-400/70" />
          </div>
          <span className="text-xs text-muted-foreground font-mono">
            ascii-art — {selectedFont.name} font
          </span>
          <div className="flex gap-2">
            <Button
              variant="ghost"
              size="sm"
              onClick={handleClear}
              className="h-7 px-2 text-xs text-muted-foreground hover:text-foreground"
            >
              <RefreshCw className="h-3 w-3 mr-1" />
              Clear
            </Button>
            <Button
              size="sm"
              onClick={handleCopy}
              disabled={!asciiOutput}
              className="h-7 px-3 text-xs bg-gradient-to-r from-brand to-brand-accent text-white shadow-sm shadow-brand/25"
            >
              {copied ? (
                <>
                  <Check className="h-3 w-3 mr-1" />
                  Copied!
                </>
              ) : (
                <>
                  <Copy className="h-3 w-3 mr-1" />
                  Copy
                </>
              )}
            </Button>
          </div>
        </div>

        {/* Output */}
        <div className="p-6 min-h-[160px] overflow-x-auto">
          {asciiOutput ? (
            <pre className="font-mono text-sm leading-tight text-foreground whitespace-pre select-all">
              {asciiOutput}
            </pre>
          ) : (
            <p className="text-muted-foreground text-sm text-center py-12">
              Start typing above to generate ASCII art.
            </p>
          )}
        </div>
      </motion.div>

      {/* Tip */}
      <motion.p
        initial={{ opacity: 0 }}
        animate={{ opacity: 1 }}
        transition={{ delay: 0.3 }}
        className="text-xs text-center text-muted-foreground"
      >
        Tip: Wrap in a{" "}
        <code className="font-mono bg-muted px-1 rounded">``` ```</code> code
        block when pasting into a Markdown README to preserve spacing.
      </motion.p>
    </div>
  );
}
