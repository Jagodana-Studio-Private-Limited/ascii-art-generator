function renderAscii(text: string, font: AsciiFont): string {
  if (!text.trim()) return "";
  const chars = text.toUpperCase().split("").slice(0, 50);
  const rows: string[] = Array(font.height).fill("");

  for (const char of chars) {
    const glyphs = font.render(char);
    // Pad or trim to font height
    const padded = Array(font.height)
      .fill("")
      .map((_, i) => glyphs[i] ?? " ".repeat((glyphs[0] ?? " ").length));

    // Find max width for this char
    const maxWidth = Math.max(...padded.map((g) => g.length));

    for (let r = 0; r < font.height; r++) {
      const g = padded[r] ?? "";
      rows[r] += g.padEnd(maxWidth, " ") + " ";
    }
  }

  return rows.join("\n");
}

